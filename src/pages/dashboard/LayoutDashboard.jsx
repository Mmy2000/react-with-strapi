import {
  IconButton,
  Avatar,
  Box,
  CloseButton,
  Flex,
  HStack,
  VStack,
  Icon,
  useColorModeValue,
  Text,
  Drawer,
  DrawerContent,
  useDisclosure,
  Menu,
  MenuButton,
  MenuDivider,
  MenuItem,
  MenuList,
} from "@chakra-ui/react";
import { FiHome, FiMenu, FiBell, FiChevronDown } from "react-icons/fi";
import { Outlet, useLocation } from "react-router-dom";
import { Link as RouterLink } from "react-router-dom";
import { AiOutlineProduct } from "react-icons/ai";
import { BiCategoryAlt } from "react-icons/bi";

// Define your links here
const LinkItems = [
  { to: "/dashboard", name: "Dashboard", icon: FiHome },
  { to: "/dashboard/products", name: "Products", icon: AiOutlineProduct },
  { to: "/dashboard/categories", name: "Categories", icon: BiCategoryAlt },
];

// Sidebar content with dynamic links
const SidebarContent = ({ onClose, ...rest }) => {
  const location = useLocation(); // Use useLocation to get the current route

  return (
    <Box
      transition="3s ease"
      bg={useColorModeValue("white", "gray.900")}
      borderRight="1px"
      borderRightColor={useColorModeValue("gray.200", "gray.700")}
      w={{ base: "full", md: 60 }}
      pos="fixed"
      h="full"
      {...rest}
    >
      <Flex h="20" alignItems="center" mx="8" justifyContent="space-between">
        <Text fontSize="2xl" fontFamily="monospace" fontWeight="bold">
          Logo
        </Text>
        <CloseButton display={{ base: "flex", md: "none" }} onClick={onClose} />
      </Flex>
      {LinkItems.map((link) => (
        <NavItem
          key={link.name}
          icon={link.icon}
          to={link.to}
          isActive={location.pathname === link.to} // Check if the current path matches the link
        >
          {link.name}
        </NavItem>
      ))}
    </Box>
  );
};

// NavItem component with active link styles
const NavItem = ({ to, icon, children, isActive, ...rest }) => {
  return (
    <Box
      as={RouterLink}
      to={to}
      style={{ textDecoration: "none" }}
      _focus={{ boxShadow: "none" }}
    >
      <Flex
        align="center"
        p="4"
        mx="4"
        borderRadius="lg"
        role="group"
        cursor="pointer"
        bg={isActive ? "cyan.400" : "transparent"} // Active link background
        color={isActive ? "white" : "inherit"} // Active link text color
        _hover={{
          bg: "cyan.400",
          color: "white",
        }}
        {...rest}
      >
        {icon && (
          <Icon
            mr="4"
            fontSize="16"
            _groupHover={{
              color: "white",
            }}
            as={icon}
          />
        )}
        {children}
      </Flex>
    </Box>
  );
};

